  push rdx
  push rsi
  push rdi
  push rbp
  push r8
  push r9
  push r10
  push r11
  lea rdi, [rsp+11*8]
  mov rsi, rax
  mov rdx, [rsp+10*8] ; Error code
  shl rax, 3
  add rax, IRQHandlers
  cld
  call [rax]
  pop r11
  pop r10
  pop r9
  pop r8
  pop rbp
  pop rdi
  pop rsi
  pop rdx
  pop rcx
  pop rax
  xchg bx, bx
  add rsp, 8 ; Pop the error code off the stack
  iretq

